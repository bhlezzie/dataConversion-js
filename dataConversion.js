// Convert a String to a Number
let price = "200";

// Method 1: Using parseInt()
let priceAsNumber1 = parseInt(price);
console.log("Using parseInt:", priceAsNumber1);

// Method 2: Using Number()
let priceAsNumber2 = Number(price);
console.log("Using Number:", priceAsNumber2);

// Method 3: Using the + operator
let priceAsNumber3 = +price;
console.log("Using + operator:", priceAsNumber3);

// Convert a Number to a String
let distance = 150;

// Method 1: Using .toString()
let distanceAsString1 = distance.toString();
console.log("Using .toString:", distanceAsString1);

// Method 2: Using String()
let distanceAsString2 = String(distance);
console.log("Using String():", distanceAsString2);

// Convert a String to a Decimal Number
let weight = "72.5";

// Using parseFloat()
let weightAsDecimal = parseFloat(weight);
console.log("Using parseFloat:", weightAsDecimal);
